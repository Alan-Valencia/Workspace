# Definiendo una variable con camelCase
nombreCompletoDeTuTíoMaster = "Lucas Dalto"

# Definiendo una variable con snake_case *Recomendada Python*
nombre_completo_de_tu_tío_master = "Lucas Dalto"

# Concatenar con +
bienvenida = "Hola" + "¿Como estas?"

# Concatenar con f-strings
bienvenida = f"Hola {nombre_completo_de_tu_tío_master} ¿Como estas?"

# Operadores de pertenencia (in /not in)

print("Mario" in bienvenida)  # true
print("Mario" not in bienvenida)  # false


